package Leeson7.Homework7.Java;

public enum Month {
    JANUARY("январь"),
    FEBRUARY("февраль"),
    MARCH("март"),
    APRIL("апрель"),
    MAY("май"),
    JUNE("июнь"),
    JULY("июль"),
    AUGUST("август"),
    SEPTEMBER("сентябрь"),
    OCTOBER("октбярь"),
    NOVEMBER("ноябрь"),
    DECEMBER("декабрь");

    public String value;

    Month(String value){
        this.value = value;
    }

}
